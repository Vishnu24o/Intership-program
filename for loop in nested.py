# for loop in nested (the outer loop will perform only one variable at a time and the inner loop will perform the whole string with the outer loop first variable)


p=['a','b','c','d','e']
q=[1,2,3,4,5]
for f in p:
    for g in q:
        print (f,g)
    



w=[1,2,3,4,5,6,7]
l=['a','d','r','t']
for f in w:
    for v in l:
        print(f,v)
